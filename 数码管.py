from turtle import *
def draw(draw):
  pendown() if draw else penup()
  fd(50)
  penup()
  fd(5)
  right(90)
  fd(5)
def paint(digit):
  draw(1) if digit in [2,3,4,5,6,8,9] else draw(0)
  draw(1) if digit != 2 else draw(0)
  draw(1) if digit in [0,2,3,5,6,8,9] else draw(0)
  draw(1) if digit in [0,2,6,8] else draw(0)
  fd(-5)
  left(90)
  draw(1) if digit in [0,4,5,6,8,9] else draw(0)
  draw(1) if digit in [0,2,3,5,6,7,8,9] else draw(0)
  draw(1) if digit in [0,1,2,3,4,7,8,9] else draw(0)
  right(90)
  fd(5)
  right(90)
  fd(5)
setup(1200,600,0,0)
penup()
fd(-100)
paint(2)
fd(50)
paint(0)
fd(50)
paint(1)
fd(50)
paint(9)
